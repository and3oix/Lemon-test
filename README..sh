## INSTALLATION [Termux] :

* `apt-get update -y`
* `apt-get upgrade -y`
* `apt install python python2 -y`
* `pip install lolcat`
* `git clone https://github.com/h4ck3r0/Lemon-termux`
* `cd Lemon-termux`
* `bash lemon.sh`
* `Java 8 Added Successfully`

#### Install Java To Fix Build Error

[+]-- Type lemon To Run

